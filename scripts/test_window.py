#!/usr/bin/env python3
"""测试 run_briefing.py 的智能窗口计算逻辑。

覆盖场景:
  - 普通工作日（周二~周五）→ days=1
  - 周末（周六/周日）→ 跳过
  - 周一 → days=3，覆盖周五~周日
  - 各法定节假日过渡
  - 调休补班日
"""

import sys
from datetime import date
from pathlib import Path

# 确保可以 import run_briefing
sys.path.insert(0, str(Path(__file__).resolve().parent))
from run_briefing import is_off_day, calc_window, _load_dates, HOLIDAYS_FILE, WORKDAYS_FILE

holidays = _load_dates(HOLIDAYS_FILE)
workdays = _load_dates(WORKDAYS_FILE)

passed = 0
failed = 0


def check(today_str: str, expected_days: int | None, label: str):
    """expected_days=None 表示应跳过（非工作日）。"""
    global passed, failed
    today = date.fromisoformat(today_str)
    is_off = is_off_day(today, holidays, workdays)

    if expected_days is None:
        if is_off:
            passed += 1
            print(f"  ✓ {today_str} {label} → 跳过")
        else:
            failed += 1
            print(f"  ✗ {today_str} {label} → 预期跳过，但判为工作日")
        return

    if is_off:
        failed += 1
        print(f"  ✗ {today_str} {label} → 预期工作日，但判为非工作日（跳过）")
        return

    days, ref_date, window_start = calc_window(today, holidays, workdays)
    if days == expected_days:
        passed += 1
        print(f"  ✓ {today_str} {label} → days={days}  范围=[{window_start} ~ {ref_date}]")
    else:
        failed += 1
        print(f"  ✗ {today_str} {label} → days={days} (预期 {expected_days})  范围=[{window_start} ~ {ref_date}]")


# ═══════════════════════════════════════════════════
# 1. 普通周（2026-07-06 这周）
# ═══════════════════════════════════════════════════
print("── 普通周 ──")
check("2026-07-07", 1, "周二 → 覆盖周一")
check("2026-07-08", 1, "周三 → 覆盖周二")
check("2026-07-09", 1, "周四 → 覆盖周三")
check("2026-07-10", 1, "周五 → 覆盖周四")
check("2026-07-11", None, "周六 → 跳过")
check("2026-07-12", None, "周日 → 跳过")
check("2026-07-13", 3, "周一 → 覆盖周五+周末(3天)")

# ═══════════════════════════════════════════════════
# 2. 元旦 (1/1-3 假期, 1/4 补班)
# ═══════════════════════════════════════════════════
print("\n── 元旦 ──")
check("2025-12-31", 1, "假期前最后一个工作日")
check("2026-01-01", None, "元旦假期")
check("2026-01-02", None, "元旦假期")
check("2026-01-03", None, "元旦假期(周六)")
check("2026-01-04", 4, "周日补班 → 覆盖12/31+假期3天")
check("2026-01-05", 1, "周一 → 覆盖1/4补班日")

# ═══════════════════════════════════════════════════
# 3. 春节 (2/15-23 假期, 2/14 & 2/28 补班)
# ═══════════════════════════════════════════════════
print("\n── 春节 ──")
check("2026-02-13", 1, "假期前最后一个周五")
check("2026-02-14", 1, "周六补班 → 覆盖周五")
check("2026-02-15", None, "春节假期(周日)")
check("2026-02-20", None, "春节假期中(周五)")
check("2026-02-23", None, "春节假期最后(周一)")
check("2026-02-24", 10, "节后首个工作日(周二) → 覆盖 2/14 + 9天假期")
check("2026-02-27", 1, "周五 → 覆盖周四")
check("2026-02-28", 1, "周六补班 → 覆盖周五")

# ═══════════════════════════════════════════════════
# 4. 清明节 (4/4-6 假期)
# ═══════════════════════════════════════════════════
print("\n── 清明节 ──")
check("2026-04-03", 1, "假期前最后一个工作日(周五)")
check("2026-04-04", None, "清明假期(周六)")
check("2026-04-06", None, "清明假期最后(周一)")
check("2026-04-07", 4, "节后首个工作日(周二) → 覆盖4/3+假期3天")

# ═══════════════════════════════════════════════════
# 5. 劳动节 (5/1-5 假期, 5/9 补班)
# ═══════════════════════════════════════════════════
print("\n── 劳动节 ──")
check("2026-04-30", 1, "假期前最后一个工作日(周四)")
check("2026-05-01", None, "劳动节假期(周五)")
check("2026-05-05", None, "劳动节假期最后(周二)")
check("2026-05-06", 6, "节后首个工作日(周三) → 覆盖4/30+假期5天")
check("2026-05-09", 1, "周六补班 → 覆盖周五")

# ═══════════════════════════════════════════════════
# 6. 端午节 (6/19-21 假期)
# ═══════════════════════════════════════════════════
print("\n── 端午节 ──")
check("2026-06-18", 1, "假期前最后一个工作日(周四)")
check("2026-06-19", None, "端午假期(周五)")
check("2026-06-21", None, "端午假期最后(周日)")
check("2026-06-22", 4, "节后首个工作日(周一) → 覆盖6/18+假期3天")

# ═══════════════════════════════════════════════════
# 7. 中秋节 (9/25-27) + 国庆节 (10/1-7)
#    补班: 9/20, 10/10
# ═══════════════════════════════════════════════════
print("\n── 中秋+国庆 ──")
# 中秋前
check("2026-09-18", 1, "中秋前最后一个周五")
check("2026-09-20", 2, "周日补班 → 覆盖9/18(周五)+9/19(周六)")
check("2026-09-21", 1, "周一 → 覆盖9/20(补班日)")
check("2026-09-24", 1, "中秋前最后一个工作日(周四)")
check("2026-09-25", None, "中秋假期(周五)")
check("2026-09-27", None, "中秋假期最后(周日)")
check("2026-09-28", 4, "节后首个工作日(周一) → 覆盖9/24+假期3天")
# 国庆前
check("2026-09-29", 1, "周二 → 覆盖周一(9/28)")
check("2026-09-30", 1, "国庆前最后一个工作日(周三)")
check("2026-10-01", None, "国庆假期(周四)")
check("2026-10-07", None, "国庆假期最后(周三)")
check("2026-10-08", 8, "节后首个工作日(周四) → 覆盖9/30+假期7天")
check("2026-10-09", 1, "周五 → 覆盖周四(10/8)")
check("2026-10-10", 1, "周六补班 → 覆盖周五(10/9)")

# ═══════════════════════════════════════════════════
# 结果
# ═══════════════════════════════════════════════════
print(f"\n{'='*50}")
print(f"  通过: {passed}  |  失败: {failed}  |  总计: {passed + failed}")
print(f"{'='*50}")

if failed > 0:
    print("\n[FAIL] 部分测试未通过！")
    sys.exit(1)
else:
    print("[PASS] 全部测试通过")
